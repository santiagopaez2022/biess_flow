package ec.gov.iess.creditos.dinamico.dto;

import java.io.Serializable;

/**
 * Clase DTO de la categoria del proveedor
 * 
 * @author paul.sampedro
 *
 */
public class CategoriaProveedorDto implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3949257903437173298L;

	/**
	 * Es el plazo maximo que puede tener la categoria
	 */
	private Integer plazoMaximo;

	/**
	 * Es la descripcion que tiene la categoria
	 */
	private String descripcion;

	/**
	 * Codigo de la categoria
	 */
	private String codigoCategoria;
	

	public Integer getPlazoMaximo() {
		return plazoMaximo;
	}

	public void setPlazoMaximo(Integer plazoMaximo) {
		this.plazoMaximo = plazoMaximo;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getCodigoCategoria() {
		return codigoCategoria;
	}

	public void setCodigoCategoria(String codigoCategoria) {
		this.codigoCategoria = codigoCategoria;
	}



}
