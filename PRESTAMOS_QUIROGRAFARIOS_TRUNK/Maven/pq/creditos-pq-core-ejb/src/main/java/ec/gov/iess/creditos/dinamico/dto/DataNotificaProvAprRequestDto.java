package ec.gov.iess.creditos.dinamico.dto;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * Entidad que envia al servicio
 * 
 * @author Paul.Sampedro <paul.sampedro@biess.fin.ec>
 *
 */
public class DataNotificaProvAprRequestDto implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1234034342240231212L;

	/**
	 * Codigo del tipo del producto para el dinamico siempre 4 CODPRETIP
	 */
	private Long codigoProducto;
	/**
	 * codigo especial para el credito CR_CREDITO_ESPECIAL
	 */
	private Long codigoEspecial;
	/**
	 * Codigo de consulta contra el proveedor del servicio
	 */
	private String codigoContrato;
	/**
	 * Numero de prestamo generado NUMPREAFI
	 */
	private Long numeroPrestamo;
	/**
	 * RUC de la empresa proveedora
	 */
	private String rucEmpresa;

	/**
	 * Codigo codprecla del prestamo
	 */
	private Long codPreCla;

	/**
	 * Codigo ordpreafi del prestamo
	 */
	private Long ordPreAfi;
	/**
	 * valor que se obtuvo de la consulta del proveedor para realizar el credito
	 */
	private BigDecimal valorPaquete;

	/**
	 * Valor que va ser desembolsado al proveedor
	 */
	private BigDecimal valorDesembolso;
	/**
	 * Fecha que se realizo la aprobacion del credito
	 */
	private String fechaAprobacion;

	/**
	 * documento de aceptacion
	 */
	private String docAceptacion;
	
	/**
	 * Codigo de la categoria 
	 * PREGRADO POSTGRADO,ETC
	 */
	private String codCategoria;

	public Long getCodigoProducto() {
		return codigoProducto;
	}

	public void setCodigoProducto(Long codigoProducto) {
		this.codigoProducto = codigoProducto;
	}

	public Long getCodigoEspecial() {
		return codigoEspecial;
	}

	public void setCodigoEspecial(Long codigoEspecial) {
		this.codigoEspecial = codigoEspecial;
	}

	public String getCodigoContrato() {
		return codigoContrato;
	}

	public void setCodigoContrato(String codigoContrato) {
		this.codigoContrato = codigoContrato;
	}

	public Long getNumeroPrestamo() {
		return numeroPrestamo;
	}

	public void setNumeroPrestamo(Long numeroPrestamo) {
		this.numeroPrestamo = numeroPrestamo;
	}

	public String getRucEmpresa() {
		return rucEmpresa;
	}

	public void setRucEmpresa(String rucEmpresa) {
		this.rucEmpresa = rucEmpresa;
	}

	public BigDecimal getValorPaquete() {
		return valorPaquete;
	}

	public void setValorPaquete(BigDecimal valorPaquete) {
		this.valorPaquete = valorPaquete;
	}

	public String getFechaAprobacion() {
		return fechaAprobacion;
	}

	public void setFechaAprobacion(String fechaAprobacion) {
		this.fechaAprobacion = fechaAprobacion;
	}

	public Long getCodPreCla() {
		return codPreCla;
	}

	public void setCodPreCla(Long codPreCla) {
		this.codPreCla = codPreCla;
	}

	public Long getOrdPreAfi() {
		return ordPreAfi;
	}

	public void setOrdPreAfi(Long ordPreAfi) {
		this.ordPreAfi = ordPreAfi;
	}

	public BigDecimal getValorDesembolso() {
		return valorDesembolso;
	}

	public void setValorDesembolso(BigDecimal valorDesembolso) {
		this.valorDesembolso = valorDesembolso;
	}

	public String getDocAceptacion() {
		return docAceptacion;
	}

	public void setDocAceptacion(String docAceptacion) {
		this.docAceptacion = docAceptacion;
	}

	public String getCodCategoria() {
		return codCategoria;
	}

	public void setCodCategoria(String codCategoria) {
		this.codCategoria = codCategoria;
	}


}
