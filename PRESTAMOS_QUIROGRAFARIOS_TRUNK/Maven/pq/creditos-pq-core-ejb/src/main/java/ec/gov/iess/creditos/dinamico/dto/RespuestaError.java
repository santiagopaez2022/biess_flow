package ec.gov.iess.creditos.dinamico.dto;

import java.io.Serializable;

/**
 * @author Paul.Sampedro <paul.sampedro@biess.fin.ec>
 *
 */
public class RespuestaError implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7646024822240237037L;
	/**
	 * codigoErr
	 */
	private String codigoErr;
	/**
	 * descripcionErr
	 */
	private String descripcionErr;

	public String getCodigoErr() {
		return codigoErr;
	}

	public void setCodigoErr(final String codigoErr) {
		this.codigoErr = codigoErr;
	}

	public String getDescripcionErr() {
		return descripcionErr;
	}

	public void setDescripcionErr(final String descripcionErr) {
		this.descripcionErr = descripcionErr;
	}

}
