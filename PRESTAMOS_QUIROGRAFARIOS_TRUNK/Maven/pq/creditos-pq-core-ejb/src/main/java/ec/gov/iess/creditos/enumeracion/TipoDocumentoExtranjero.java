package ec.gov.iess.creditos.enumeracion;

/**
 * Enumeracion para Tipos de documentos de extranjeros.
 * 
 * @author diego.iza.
 * 
 * @version 1.0
 */
public enum TipoDocumentoExtranjero {
	PAS("PAS", "Pasaporte"), CLB("CLB", "Carnet Laboral"), CDR("CDR", "Carnet de Refugiado");

	/**
	 * 
	 */
	private String codigo;

	/**
	 * 
	 * @return <String>.
	 */
	private String descripcion;

	/**
	 * @param codigo
	 * @param descripcion
	 */
	private TipoDocumentoExtranjero(String codigo, String descripcion) {
		this.codigo = codigo;
		this.descripcion = descripcion;
	}

	/**
	 * @return the codigo
	 */
	public String getCodigo() {
		return codigo;
	}

	/**
	 * @param codigo
	 *            the codigo to set
	 */
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
		}

	/**
	 * @param descripcion
	 *            the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
}