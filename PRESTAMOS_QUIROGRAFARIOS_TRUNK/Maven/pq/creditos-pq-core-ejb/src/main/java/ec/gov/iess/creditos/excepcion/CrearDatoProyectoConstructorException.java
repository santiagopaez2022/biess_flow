package ec.gov.iess.creditos.excepcion;

import javax.ejb.ApplicationException;

/**
 * 
 * @author jsanchez
 * 
 */
@ApplicationException(rollback = true)
public class CrearDatoProyectoConstructorException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 2320965801833495983L;

	public CrearDatoProyectoConstructorException() {
		super();
	}

	public CrearDatoProyectoConstructorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CrearDatoProyectoConstructorException(String arg0) {
		super(arg0);
	}

	public CrearDatoProyectoConstructorException(Throwable arg0) {
		super(arg0);
	}

}