package ec.gov.iess.creditos.excepcion;

/** 
 * <b>
 * Excepcion para parametro no encontrado
 * </b>
 *  
 * @author Ricardo Titua√±a
 * 
*/
public class ParametroNoEncontradoException extends Exception {

	private static final long serialVersionUID = 5856357916293148464L;

	public ParametroNoEncontradoException() {
	}

	public ParametroNoEncontradoException(String arg0) {
		super(arg0);
	}

	public ParametroNoEncontradoException(Throwable arg0) {
		super(arg0);
	}

	public ParametroNoEncontradoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
