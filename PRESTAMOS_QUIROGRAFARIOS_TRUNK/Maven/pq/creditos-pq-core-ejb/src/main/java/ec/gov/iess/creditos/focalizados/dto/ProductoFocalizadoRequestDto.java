package ec.gov.iess.creditos.focalizados.dto;

import java.io.Serializable;

/**
 * Request para consulta de productos para pq focalizados
 * 
 * @author hugo.mora
 *
 */
public class ProductoFocalizadoRequestDto extends CodigoMeerFocalizadoRequestDto implements Serializable {

	private static final long serialVersionUID = 1L;

	private Integer tipoProducto;
	
	private Integer codigoProductoMeer;

	// Getters and setters
	public Integer getTipoProducto() {
		return tipoProducto;
	}

	public void setTipoProducto(Integer tipoProducto) {
		this.tipoProducto = tipoProducto;
	}

	public Integer getCodigoProductoMeer() {
		return codigoProductoMeer;
	}

	public void setCodigoProductoMeer(Integer codigoProductoMeer) {
		this.codigoProductoMeer = codigoProductoMeer;
	}

}
