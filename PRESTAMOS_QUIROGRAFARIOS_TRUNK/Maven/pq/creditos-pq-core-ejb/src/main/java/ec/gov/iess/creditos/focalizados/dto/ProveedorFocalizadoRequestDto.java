package ec.gov.iess.creditos.focalizados.dto;

import java.io.Serializable;

/**
 * Request para proveedores de prestamos focalizados
 * 
 * @author hugo.mora
 *
 */
public class ProveedorFocalizadoRequestDto extends CodigoMeerFocalizadoRequestDto implements Serializable {

	private static final long serialVersionUID = 1L;

	private String codigoUbicacionInec;
	
	private String estado;

	// Getters and setters
	public String getCodigoUbicacionInec() {
		return codigoUbicacionInec;
	}

	public void setCodigoUbicacionInec(String codigoUbicacionInec) {
		this.codigoUbicacionInec = codigoUbicacionInec;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

}
