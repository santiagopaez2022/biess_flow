package ec.gov.iess.creditos.modelo.dto;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * @author cvillarreal
 * 
 */
public class CondicionCalculo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5385677445760623402L;

	private int esperanzaVida;

	private int edadActualAnios;
	
	private int edadMesesAnios;

	private int plazoMaximo;

	private BigDecimal tasaInteres;

	private BigDecimal porcentajeSeguroDesgravamen;

	/**
	 * 
	 */
	public CondicionCalculo() {
	}

	public CondicionCalculo(int esperanzaVidaNew, int edadActualNew,
			int plazoMaximoNew, BigDecimal tasaInterezNew,
			BigDecimal porcentajeSeguroDesgravamenNew) {
		this.esperanzaVida = esperanzaVidaNew;
		this.edadActualAnios = edadActualNew;
		this.plazoMaximo = plazoMaximoNew;
		this.tasaInteres = tasaInterezNew;
	}

	/**
	 * @return the esperanzaVida
	 */
	public int getEsperanzaVida() {
		return esperanzaVida;
	}

	/**
	 * @param esperanzaVida the esperanzaVida to set
	 */
	public void setEsperanzaVida(int esperanzaVida) {
		this.esperanzaVida = esperanzaVida;
	}

	/**
	 * @return the edadActualAnios
	 */
	public int getEdadActualAnios() {
		return edadActualAnios;
	}

	/**
	 * @param edadActualAnios the edadActualAnios to set
	 */
	public void setEdadActualAnios(int edadActualAnios) {
		this.edadActualAnios = edadActualAnios;
	}

	/**
	 * @return the edadMesesAnios
	 */
	public int getEdadMesesAnios() {
		return edadMesesAnios;
	}

	/**
	 * @param edadMesesAnios the edadMesesAnios to set
	 */
	public void setEdadMesesAnios(int edadMesesAnios) {
		this.edadMesesAnios = edadMesesAnios;
	}

	/**
	 * @return the plazoMaximo
	 */
	public int getPlazoMaximo() {
		return plazoMaximo;
	}

	/**
	 * @param plazoMaximo the plazoMaximo to set
	 */
	public void setPlazoMaximo(int plazoMaximo) {
		this.plazoMaximo = plazoMaximo;
	}

	/**
	 * @return the tasaInteres
	 */
	public BigDecimal getTasaInteres() {
		return tasaInteres;
	}

	/**
	 * @param tasaInteres the tasaInteres to set
	 */
	public void setTasaInteres(BigDecimal tasaInteres) {
		this.tasaInteres = tasaInteres;
	}

	/**
	 * @return the porcentajeSeguroDesgravamen
	 */
	public BigDecimal getPorcentajeSeguroDesgravamen() {
		return porcentajeSeguroDesgravamen;
	}

	/**
	 * @param porcentajeSeguroDesgravamen the porcentajeSeguroDesgravamen to set
	 */
	public void setPorcentajeSeguroDesgravamen(
			BigDecimal porcentajeSeguroDesgravamen) {
		this.porcentajeSeguroDesgravamen = porcentajeSeguroDesgravamen;
	}

	/**
	 * @return the serialVersionUID
	 */
	public static long getSerialVersionUID() {
		return serialVersionUID;
	}


}
