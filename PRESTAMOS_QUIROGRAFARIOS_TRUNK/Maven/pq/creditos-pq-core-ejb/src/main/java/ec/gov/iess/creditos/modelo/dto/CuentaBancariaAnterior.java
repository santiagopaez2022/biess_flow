package ec.gov.iess.creditos.modelo.dto;

import java.util.Date;



/**
 * Datos de cuenta bancaria anterior
 * 
 * @author pjarrin
 * 
 */
public class CuentaBancariaAnterior {

	
	private String tipoCuentaAnterior;
	private String numeroCuentaAnterior;
	private String entidadFinancieraAnterior;
	private Date fechaRegistroAnterior;

	public String getTipoCuentaAnterior() {
		return tipoCuentaAnterior;
	}

	public void setTipoCuentaAnterior(String tipoCuentaAnterior) {
		this.tipoCuentaAnterior = tipoCuentaAnterior;
	}

	public String getNumeroCuentaAnterior() {
		return numeroCuentaAnterior;
	}

	public void setNumeroCuentaAnterior(String numeroCuentaAnterior) {
		this.numeroCuentaAnterior = numeroCuentaAnterior;
	}

	public String getEntidadFinancieraAnterior() {
		return entidadFinancieraAnterior;
	}

	public void setEntidadFinancieraAnterior(String entidadFinancieraAnterior) {
		this.entidadFinancieraAnterior = entidadFinancieraAnterior;
	}

	public void setFechaRegistroAnterior(Date fechaRegistroAnterior) {
		this.fechaRegistroAnterior = fechaRegistroAnterior;
	}

	public Date getFechaRegistroAnterior() {
		return fechaRegistroAnterior;
	}

}