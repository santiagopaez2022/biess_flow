package ec.gov.iess.creditos.modelo.dto;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * @author cvillarreal
 * 
 */
public class DetalleGarantia implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5661554351510865940L;
	private String nombreGarantia;
	private BigDecimal valorGarantia;
	private String observacion;

	public String getObservacion() {
		return observacion;
	}

	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}

	/**
	 * 
	 */
	public DetalleGarantia() {

	}

	public DetalleGarantia(String nombreGarantia, BigDecimal valorGarantia) {
		this.nombreGarantia = nombreGarantia;
		this.valorGarantia = valorGarantia;
	}

	public String getNombreGarantia() {
		return nombreGarantia;
	}

	public void setNombreGarantia(String nombreGarantia) {
		this.nombreGarantia = nombreGarantia;
	}

	public BigDecimal getValorGarantia() {
		return valorGarantia;
	}

	public void setValorGarantia(BigDecimal valorGarantia) {
		this.valorGarantia = valorGarantia;
	}

}
