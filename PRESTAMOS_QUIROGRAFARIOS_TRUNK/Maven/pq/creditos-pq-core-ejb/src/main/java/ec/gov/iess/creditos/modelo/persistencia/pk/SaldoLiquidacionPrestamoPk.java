package ec.gov.iess.creditos.modelo.persistencia.pk;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

/**
 * Incluir aquí la descripción de la clase.
 *  
 * @version $Revision: 1.1 $  [Sep 24, 2007]
 * @author pablo
 */
@Embeddable
public class SaldoLiquidacionPrestamoPk implements Serializable {

	private static final long serialVersionUID = -7942652803096115552L;

	@Column(name = "SECUENCIAL")
	private Long secuencial;

	@Column(name = "NUMDIV")
	private Integer numDiv;

	@Column(name = "CODPRETIP")
	private Integer codPreTip;

	@Column(name = "ORDPREAFI")
	private Integer ordPreAfi;

	@Column(name = "NUMPREAFI")
	private Long numPreAfi;

	@Column(name = "CODPRECLA")
	private Integer codPreCla;

	/**
	 * Returns the value of secuencial.
	 * @return secuencial
	 */
	public Long getSecuencial() {
		return secuencial;
	}

	/**
	 * Sets the value for secuencial.
	 * @param secuencial
	 */
	public void setSecuencial(Long secuencial) {
		this.secuencial = secuencial;
	}

	/**
	 * Returns the value of numDiv.
	 * @return numDiv
	 */
	public Integer getNumDiv() {
		return numDiv;
	}

	/**
	 * Sets the value for numDiv.
	 * @param numDiv
	 */
	public void setNumDiv(Integer numDiv) {
		this.numDiv = numDiv;
	}

	/**
	 * Returns the value of codPreTip.
	 * @return codPreTip
	 */
	public Integer getCodPreTip() {
		return codPreTip;
	}

	/**
	 * Sets the value for codPreTip.
	 * @param codPreTip
	 */
	public void setCodPreTip(Integer codPreTip) {
		this.codPreTip = codPreTip;
	}

	/**
	 * Returns the value of ordPreAfi.
	 * @return ordPreAfi
	 */
	public Integer getOrdPreAfi() {
		return ordPreAfi;
	}

	/**
	 * Sets the value for ordPreAfi.
	 * @param ordPreAfi
	 */
	public void setOrdPreAfi(Integer ordPreAfi) {
		this.ordPreAfi = ordPreAfi;
	}

	/**
	 * Returns the value of numPreAfi.
	 * @return numPreAfi
	 */
	public Long getNumPreAfi() {
		return numPreAfi;
	}

	/**
	 * Sets the value for numPreAfi.
	 * @param numPreAfi
	 */
	public void setNumPreAfi(Long numPreAfi) {
		this.numPreAfi = numPreAfi;
	}

	/**
	 * Returns the value of codPreCla.
	 * @return codPreCla
	 */
	public Integer getCodPreCla() {
		return codPreCla;
	}

	/**
	 * Sets the value for codPreCla.
	 * @param codPreCla
	 */
	public void setCodPreCla(Integer codPreCla) {
		this.codPreCla = codPreCla;
	}

}