package ec.gov.iess.creditos.otp.dto;

import java.io.Serializable;

/**
 * DTO para la peticion de generacion del OTP
 * 
 * @author hugo.mora
 * @date 2016/11/17
 *
 */
public class OTPGenerarRequestDto implements Serializable {

	private static final long serialVersionUID = 1L;

	private String idTransaccion;

	private String referencia;

	private NotificacionOTP notificacion;
	
	private String idNegocio;

	// Getters and setters
	public String getIdTransaccion() {
		return idTransaccion;
	}

	public void setIdTransaccion(String idTransaccion) {
		this.idTransaccion = idTransaccion;
	}

	public String getReferencia() {
		return referencia;
	}

	public void setReferencia(String referencia) {
		this.referencia = referencia;
	}

	public NotificacionOTP getNotificacion() {
		return notificacion;
	}

	public void setNotificacion(NotificacionOTP notificacion) {
		this.notificacion = notificacion;
	}

	public String getIdNegocio() {
		return idNegocio;
	}

	public void setIdNegocio(String idNegocio) {
		this.idNegocio = idNegocio;
	}

}
