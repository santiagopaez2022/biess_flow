package ec.gov.iess.creditos.turismo.dto;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * Pojo de respuesta del servicio web consulta Informacion Paquete Turistico
 * 
 * @author hugo.mora
 *
 */
public class ConsultaPaqueteTuristicoResponseDto implements Serializable {

	private static final long serialVersionUID = 1L;

	private BigDecimal valorTotalReserva;

	private String descripcionReserva;

	private String codigoRespuesta;

	private String descripcionRespuesta;

	// Getters and setters
	public String getCodigoRespuesta() {
		return codigoRespuesta;
	}

	public void setCodigoRespuesta(String codigoRespuesta) {
		this.codigoRespuesta = codigoRespuesta;
	}

	public String getDescripcionRespuesta() {
		return descripcionRespuesta;
	}

	public void setDescripcionRespuesta(String descripcionRespuesta) {
		this.descripcionRespuesta = descripcionRespuesta;
	}

	public BigDecimal getValorTotalReserva() {
		return valorTotalReserva;
	}

	public void setValorTotalReserva(BigDecimal valorTotalReserva) {
		this.valorTotalReserva = valorTotalReserva;
	}

	public String getDescripcionReserva() {
		return descripcionReserva;
	}

	public void setDescripcionReserva(String descripcionReserva) {
		this.descripcionReserva = descripcionReserva;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "PaqueteTuristicoResponseDto [valorTotalReserva=" + valorTotalReserva + ", descripcionReserva=" + descripcionReserva
				+ " ,codigoRespuesta=" + codigoRespuesta + ", descripcionRespuesta=" + descripcionRespuesta + "]";
	}

}
