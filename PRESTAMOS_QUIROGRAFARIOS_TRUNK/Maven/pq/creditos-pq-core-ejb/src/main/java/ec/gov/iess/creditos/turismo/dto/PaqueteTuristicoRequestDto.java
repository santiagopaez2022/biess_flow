package ec.gov.iess.creditos.turismo.dto;

import java.io.Serializable;

/**
 * Pojo de respuesta del servicio web Informacion Paquete Turistico
 * 
 * @author hugo.mora
 *
 */
public class PaqueteTuristicoRequestDto implements Serializable {

	private static final long serialVersionUID = 1L;

	private String codigoReserva;

	private String cedula;

	// Getters and setters
	public String getCodigoReserva() {
		return codigoReserva;
	}

	public void setCodigoReserva(String codigoReserva) {
		this.codigoReserva = codigoReserva;
	}

	public String getCedula() {
		return cedula;
	}

	public void setCedula(String cedula) {
		this.cedula = cedula;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "PaqueteTuristicoRequestDto [cedula=" + cedula + ", codigoReserva=" + codigoReserva + "]";
	}

}
