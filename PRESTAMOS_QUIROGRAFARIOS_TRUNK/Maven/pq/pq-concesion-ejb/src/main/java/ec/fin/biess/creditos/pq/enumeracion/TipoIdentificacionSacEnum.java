package ec.fin.biess.creditos.pq.enumeracion;

/**
 * Enumeracion con tipo de identificacion para el SAC
 * 
 * @author hugo.mora
 * @date 2018/08/31
 *
 */
public enum TipoIdentificacionSacEnum {

	CEDULA("C"), PASAPORTE("P"), RUC("R"), REFUGIADO("N");

	private TipoIdentificacionSacEnum(String codigo) {
		this.codigo = codigo;
	}

	private String codigo;

	public String getCodigo() {
		return codigo;
	}

}
