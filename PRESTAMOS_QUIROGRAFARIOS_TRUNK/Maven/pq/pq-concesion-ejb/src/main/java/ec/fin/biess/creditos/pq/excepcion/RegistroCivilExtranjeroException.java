package ec.fin.biess.creditos.pq.excepcion;

/**
 * Excepcion para consultas de informacion de Refugiados Extarngeros de la tabla de registro civil.
 * 
 * @author diego.iza.
 * 
 * @version 1.0
 */
public class RegistroCivilExtranjeroException extends Exception {

	/**
	 * Serial.
	 */
	private static final long serialVersionUID = 1L;


	/**
	 * @param message
	 */
	public RegistroCivilExtranjeroException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public RegistroCivilExtranjeroException(Throwable cause) {
		super(cause);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public RegistroCivilExtranjeroException(String message, Throwable cause) {
		super(message, cause);
	}

}
