package ec.fin.biess.creditos.pq.excepcion;

public class TablaAmortizacionException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	public TablaAmortizacionException(){
		
	}
	
	public TablaAmortizacionException(String mensaje){
		super(mensaje);
	}
	
	public TablaAmortizacionException(Throwable cause){
		super(cause);
	}
	
	public TablaAmortizacionException(String mensaje, Throwable cause){
		super(mensaje, cause);
	}

}
