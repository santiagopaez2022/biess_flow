package ec.fin.biess.creditos.pq.modelo.dto;

import java.io.Serializable;

import ec.gov.iess.creditos.pq.dto.InformacionAportacionResponseDto;

/**
 * DTO para informacion de consumos de servicios web del IESS
 * 
 * @author hugo.mora
 *
 */
public class InformacionIessServicioDto implements Serializable {

	private static final long serialVersionUID = 1L;

	private String cedula;

	private boolean productoBiessEmergente;

	private InformacionAportacionResponseDto informacionAportacionResponse;

	private InformacionCesantia informacionCesantia;

	private InformacionPrestacionPensionado informacionPrestacionPensionado;

	// Metodos SET y GET

	public String getCedula() {
		return cedula;
	}

	public void setCedula(String cedula) {
		this.cedula = cedula;
	}

	public boolean isProductoBiessEmergente() {
		return productoBiessEmergente;
	}

	public void setProductoBiessEmergente(boolean productoBiessEmergente) {
		this.productoBiessEmergente = productoBiessEmergente;
	}

	public InformacionAportacionResponseDto getInformacionAportacionResponse() {
		return informacionAportacionResponse;
	}

	public void setInformacionAportacionResponse(InformacionAportacionResponseDto informacionAportacionResponse) {
		this.informacionAportacionResponse = informacionAportacionResponse;
	}

	public InformacionCesantia getInformacionCesantia() {
		return informacionCesantia;
	}

	public void setInformacionCesantia(InformacionCesantia informacionCesantia) {
		this.informacionCesantia = informacionCesantia;
	}

	public InformacionPrestacionPensionado getInformacionPrestacionPensionado() {
		return informacionPrestacionPensionado;
	}

	public void setInformacionPrestacionPensionado(InformacionPrestacionPensionado informacionPrestacionPensionado) {
		this.informacionPrestacionPensionado = informacionPrestacionPensionado;
	}
}
