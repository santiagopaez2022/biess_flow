package ec.fin.biess.creditos.pq.modelo.dto;

import java.io.Serializable;

/**
 * Dto para almacenar la respuesta de parametro
 * @author cristian.yaselga
 *
 */
public class ParametroResponseDto implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2092053156868809900L;

	private Object valor;
	private String mensaje;
	private String codigoRespuesta;
	private String mensajeRespuesta;

	public Object getValor() {
		return valor;
	}

	public void setValor(Object valor) {
		this.valor = valor;
	}

	public String getMensaje() {
		return mensaje;
	}

	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}

	public String getCodigoRespuesta() {
		return codigoRespuesta;
	}

	public void setCodigoRespuesta(String codigoRespuesta) {
		this.codigoRespuesta = codigoRespuesta;
	}

	public String getMensajeRespuesta() {
		return mensajeRespuesta;
	}

	public void setMensajeRespuesta(String mensajeRespuesta) {
		this.mensajeRespuesta = mensajeRespuesta;
	}
}
