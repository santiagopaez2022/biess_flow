package ec.gov.iess.creditos.pq.dto;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * Contiene informacion de cada dividendo de la tabla de amortizacion obtenida del SAC
 * 
 * @author hugo.mora
 * @date 31/08/2018
 *
 */
public class DividendoDto implements Serializable {

	private static final long serialVersionUID = 1L;

	// Numero de la cuota o dividendo
	private int numeroDividendo;

	private BigDecimal capital;

	private BigDecimal interes;
	
	private BigDecimal periodoGracia;

	private BigDecimal seguroDesgravamen;

	private BigDecimal total;

	private BigDecimal capitalReducido;

	private String fechaCancelacion;

	private String estado;

	// Getters and setters
	public int getNumeroDividendo() {
		return numeroDividendo;
	}

	public void setNumeroDividendo(int numeroDividendo) {
		this.numeroDividendo = numeroDividendo;
	}

	public BigDecimal getCapital() {
		return capital;
	}

	public void setCapital(BigDecimal capital) {
		this.capital = capital;
	}

	public BigDecimal getInteres() {
		return interes;
	}

	public void setInteres(BigDecimal interes) {
		this.interes = interes;
	}

	public BigDecimal getSeguroDesgravamen() {
		return seguroDesgravamen;
	}

	public void setSeguroDesgravamen(BigDecimal seguroDesgravamen) {
		this.seguroDesgravamen = seguroDesgravamen;
	}

	public BigDecimal getTotal() {
		return total;
	}

	public void setTotal(BigDecimal total) {
		this.total = total;
	}

	public BigDecimal getCapitalReducido() {
		return capitalReducido;
	}

	public void setCapitalReducido(BigDecimal capitalReducido) {
		this.capitalReducido = capitalReducido;
	}

	public String getFechaCancelacion() {
		return fechaCancelacion;
	}

	public void setFechaCancelacion(String fechaCancelacion) {
		this.fechaCancelacion = fechaCancelacion;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public BigDecimal getPeriodoGracia() {
		return periodoGracia;
	}

	public void setPeriodoGracia(BigDecimal periodoGracia) {
		this.periodoGracia = periodoGracia;
	}

}
