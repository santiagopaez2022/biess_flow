package ec.gov.iess.creditos.pq.dto;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * DTO que devuelve valores referentes a la validacion del monto minimo
 * 
 * @author hugo.mora
 * @date 2017/07/05
 *
 */
public class EvaluaReglaMontoMinimoDto implements Serializable {

	private static final long serialVersionUID = 1L;

	private boolean cumpleMontoMinimo;

	private BigDecimal valorMinimoSBU;

	private BigDecimal valorSBUMitad;

	// Getters and setters
	public boolean isCumpleMontoMinimo() {
		return cumpleMontoMinimo;
	}

	public void setCumpleMontoMinimo(boolean cumpleMontoMinimo) {
		this.cumpleMontoMinimo = cumpleMontoMinimo;
	}

	public BigDecimal getValorMinimoSBU() {
		return valorMinimoSBU;
	}

	public void setValorMinimoSBU(BigDecimal valorMinimoSBU) {
		this.valorMinimoSBU = valorMinimoSBU;
	}

	public BigDecimal getValorSBUMitad() {
		return valorSBUMitad;
	}

	public void setValorSBUMitad(BigDecimal valorSBUMitad) {
		this.valorSBUMitad = valorSBUMitad;
	}

}
