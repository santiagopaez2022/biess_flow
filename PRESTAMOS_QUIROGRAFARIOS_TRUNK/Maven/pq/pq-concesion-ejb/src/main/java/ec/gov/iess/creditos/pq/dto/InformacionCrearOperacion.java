package ec.gov.iess.creditos.pq.dto;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * DTO con informacion de respuesta de listado de creditos exigibles, si esta en
 * mora y otros datos
 * 
 * @author hugo.mora
 * @date 03/09/2018
 *
 */
public class InformacionCrearOperacion implements Serializable {

	private static final long serialVersionUID = 21212347899600056L;

	private BigDecimal numeroOperacionSAC;

	private String estadoOperacion;

	public BigDecimal getNumeroOperacionSAC() {
		return numeroOperacionSAC;
	}

	public void setNumeroOperacionSAC(BigDecimal numeroOperacionSAC) {
		this.numeroOperacionSAC = numeroOperacionSAC;
	}

	public String getEstadoOperacion() {
		return estadoOperacion;
	}

	public void setEstadoOperacion(String estadoOperacion) {
		this.estadoOperacion = estadoOperacion;
	}

}
