package ec.gov.iess.creditos.pq.dto;

import java.io.Serializable;

/**
 * Dto de peticion para informacion de garantias comprometidas del SAC
 * 
 * @author hugo.mora
 * @date 2018/08/31
 *
 */
public class InformacionCreditosGarantiasRequestDto implements Serializable {

	private static final long serialVersionUID = 8804484989444200512L;

	private String tipoIdentificacion;

	private String numeroIdentificacion;
	
	private Long numeroOperacion;

	// Getters and setters
	public String getTipoIdentificacion() {
		return tipoIdentificacion;
	}

	public void setTipoIdentificacion(String tipoIdentificacion) {
		this.tipoIdentificacion = tipoIdentificacion;
	}

	public String getNumeroIdentificacion() {
		return numeroIdentificacion;
	}

	public void setNumeroIdentificacion(String numeroIdentificacion) {
		this.numeroIdentificacion = numeroIdentificacion;
	}

	public Long getNumeroOperacion() {
		return numeroOperacion;
	}

	public void setNumeroOperacion(Long numeroOperacion) {
		this.numeroOperacion = numeroOperacion;
	}

	

}
