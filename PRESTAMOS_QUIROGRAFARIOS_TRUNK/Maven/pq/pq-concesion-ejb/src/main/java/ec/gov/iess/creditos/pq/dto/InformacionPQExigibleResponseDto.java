package ec.gov.iess.creditos.pq.dto;

import java.io.Serializable;

/**
 * DTO con informacion de respuesta de listado de creditos exigibles, si esta en mora y otros datos
 * 
 * @author hugo.mora
 * @date 2018/08/31
 *
 */
public class InformacionPQExigibleResponseDto implements Serializable {

	private static final long serialVersionUID = -9212487805684100L;

	private InformacionPQExigible informacionPQExigible;

	private String codigoRespuesta;

	private String mensajeRespuesta;

	// Getters and setters
	public InformacionPQExigible getInformacionPQExigible() {
		return informacionPQExigible;
	}

	public void setInformacionPQExigible(InformacionPQExigible informacionPQExigible) {
		this.informacionPQExigible = informacionPQExigible;
	}

	public String getCodigoRespuesta() {
		return codigoRespuesta;
	}

	public void setCodigoRespuesta(String codigoRespuesta) {
		this.codigoRespuesta = codigoRespuesta;
	}

	public String getMensajeRespuesta() {
		return mensajeRespuesta;
	}

	public void setMensajeRespuesta(String mensajeRespuesta) {
		this.mensajeRespuesta = mensajeRespuesta;
	}

}
