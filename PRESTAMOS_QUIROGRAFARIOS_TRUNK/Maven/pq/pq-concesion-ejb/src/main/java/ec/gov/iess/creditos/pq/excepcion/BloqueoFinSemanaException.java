package ec.gov.iess.creditos.pq.excepcion;

/**
 * Excepcion usada para propagar errores al intentar crear creditos no permitidos en fines de semana
 * 
 * @author hugo.mora
 * @date 2018/04/10
 *
 */
public class BloqueoFinSemanaException extends Exception {

	private static final long serialVersionUID = 8454659848066331004L;

	/**
	 * Instancia un nuevo BloqueoFinSemanaException.
	 */
	public BloqueoFinSemanaException() {
		super();
	}

	/**
	 * Instancia un nuevo BloqueoFinSemanaException.
	 * 
	 * @param arg0
	 *            the arg0
	 * @param arg1
	 *            the arg1
	 */
	public BloqueoFinSemanaException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

	/**
	 * Instancia un nuevo BloqueoFinSemanaException.
	 * 
	 * @param arg0
	 *            the arg0
	 */
	public BloqueoFinSemanaException(final String arg0) {
		super(arg0);
	}

	/**
	 * Instancia un nuevo BloqueoFinSemanaException.
	 * 
	 * @param arg0
	 *            the arg0
	 */
	public BloqueoFinSemanaException(final Throwable arg0) {
		super(arg0);
	}

}
