package ec.gov.iess.creditos.pq.excepcion;

/**
 * @author cvillarreal
 *
 */
public class ConsultaCoeficienteException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5022282605523045322L;

	/**
	 * 
	 */
	public ConsultaCoeficienteException() {
	}

	/**
	 * @param message
	 */
	public ConsultaCoeficienteException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public ConsultaCoeficienteException(Throwable cause) {
		super(cause);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public ConsultaCoeficienteException(String message, Throwable cause) {
		super(message, cause);
	}

}
