package ec.gov.iess.creditos.pq.excepcion;

/**
 * Excepcion para manejo de errores para prestamos turisticos
 * 
 * @author hugo.mora
 *
 */
public class ConsultaDataPqDinamicoException extends Exception {

	/**
	 * Serial
	 */
	private static final long serialVersionUID = 1L;

	public ConsultaDataPqDinamicoException() {
		super();

	}

	public ConsultaDataPqDinamicoException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

	public ConsultaDataPqDinamicoException(final String arg0) {
		super(arg0);
	}

	public ConsultaDataPqDinamicoException(final Throwable arg0) {
		super(arg0);
	}

}
