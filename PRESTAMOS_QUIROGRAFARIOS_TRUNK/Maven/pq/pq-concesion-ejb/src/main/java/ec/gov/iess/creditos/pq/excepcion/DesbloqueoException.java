package ec.gov.iess.creditos.pq.excepcion;

/**
 * Class DesbloqueoException.
 * 
 * @author pjarrin
 * @revision $Revision: 1.1 $$
 */
public class DesbloqueoException extends Exception {

	private static final long serialVersionUID = 1L;

	/**
	 * Instancia un nuevo desbloqueo exception.
	 */
	public DesbloqueoException() {
		super();
	}

	/**
	 * Instancia un nuevo desbloqueo exception.
	 * 
	 * @param arg0
	 *            the arg0
	 * @param arg1
	 *            the arg1
	 */
	public DesbloqueoException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

	/**
	 * Instancia un nuevo desbloqueo exception.
	 * 
	 * @param arg0
	 *            the arg0
	 */
	public DesbloqueoException(final String arg0) {
		super(arg0);
	}

	/**
	 * Instancia un nuevo desbloqueo exception.
	 * 
	 * @param arg0
	 *            the arg0
	 */
	public DesbloqueoException(final Throwable arg0) {
		super(arg0);
	}
}