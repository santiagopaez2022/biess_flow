package ec.gov.iess.creditos.pq.excepcion;

import javax.ejb.ApplicationException;
/**
 * Clase exception para Descomprometer Garantia
 * @author Palvarez
 * 
 */
@ApplicationException(rollback = true)
public class DescomprometerGarantiaException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DescomprometerGarantiaException() {
	}

	public DescomprometerGarantiaException(String message) {
		super(message);
	}

	public DescomprometerGarantiaException(Throwable cause) {
		super(cause);
	}

	public DescomprometerGarantiaException(String message,
			Throwable cause) {
		super(message, cause);
	}

}
