package ec.gov.iess.creditos.pq.excepcion;

/**
 * Excepcion para manejo de errores para prestamos turisticos
 * 
 * @author hugo.mora
 *
 */
public class ExisteCodigoContratoException extends Exception {

	private static final long serialVersionUID = 1L;

	public ExisteCodigoContratoException() {
		super();
	}

	public ExisteCodigoContratoException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

	public ExisteCodigoContratoException(final String arg0) {
		super(arg0);
	}

	public ExisteCodigoContratoException(final Throwable arg0) {
		super(arg0);
	}

}
