package ec.gov.iess.creditos.pq.excepcion;

/**
 * @author jvaca
 *
 */
public class GarantiaException extends Exception {

	private static final long serialVersionUID = -7324743179557892321L;

	public GarantiaException() {
	}

	public GarantiaException(String message) {
		super(message);
	}

	public GarantiaException(Throwable cause) {
		super(cause);
	}

	public GarantiaException(String message, Throwable cause) {
		super(message, cause);
	}

}
