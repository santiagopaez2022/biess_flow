package ec.gov.iess.creditos.pq.excepcion;

/**
 * Excepcion usada cuando el afiliado no tiene un empleador (es cesante)
 * 
 * @author hugo.mora
 * @date 2017/07/13
 *
 */
public class NoServicioPrestadoSucursalException extends Exception {

	private static final long serialVersionUID = 1L;

	/**
	 * 
	 */
	public NoServicioPrestadoSucursalException() {
		super();
	}

	/**
	 * @param message
	 */
	public NoServicioPrestadoSucursalException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public NoServicioPrestadoSucursalException(Throwable cause) {
		super(cause);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public NoServicioPrestadoSucursalException(String message, Throwable cause) {
		super(message, cause);
	}

}
