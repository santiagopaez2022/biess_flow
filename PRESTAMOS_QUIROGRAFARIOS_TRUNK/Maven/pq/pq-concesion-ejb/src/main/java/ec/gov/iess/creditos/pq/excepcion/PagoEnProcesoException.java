package ec.gov.iess.creditos.pq.excepcion;

/**
 * @author caldaz
 *
 */
public class PagoEnProcesoException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1011895130669682333L;

	/**
	 * 
	 */
	public PagoEnProcesoException() {
		
	}

	/**
	 * @param message
	 */
	public PagoEnProcesoException(String message) {
		super(message);
		
	}

	/**
	 * @param cause
	 */
	public PagoEnProcesoException(Throwable cause) {
		super(cause);
		
	}

	/**
	 * @param message
	 * @param cause
	 */
	public PagoEnProcesoException(String message, Throwable cause) {
		super(message, cause);
		
	}

}
