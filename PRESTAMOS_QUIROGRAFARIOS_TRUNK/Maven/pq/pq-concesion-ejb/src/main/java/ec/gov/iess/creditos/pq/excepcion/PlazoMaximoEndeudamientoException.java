public class PlazoMaximoEndeudamientoException extends Exception {

	private static final long serialVersionUID = 4762500103733716365L;

	/**
	 * 
	 */
	public PlazoMaximoEndeudamientoException() {
	}

	/**
	 * @param arg0
	 */
	public PlazoMaximoEndeudamientoException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public PlazoMaximoEndeudamientoException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public PlazoMaximoEndeudamientoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}