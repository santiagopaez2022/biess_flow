package ec.gov.iess.creditos.pq.helper.reglas;

import java.math.BigDecimal;

/**
 * Clase para valores de biess emergente
 * 
 * @author hugo.mora
 * @date 13 de may. de 2016
 *
 */
public class DatosBiessEmergente {

	/**
	 * Bandera para validacion biess emergente
	 */
	private boolean validaBiessEmergente;

	/**
	 * Numero de aportaciones acumuladas
	 */
	private BigDecimal aportacionesAcumuladas;

	/**
	 * Numero de aportaciones consecutivas
	 */
	private BigDecimal aportacionesConsecutivas;
	
	/**
	 * Indica si una planillas tiene mora
	 */
	private boolean tieneMoraPlanillas;
	
	/**
	 * Indica si un comprobante tiene mora
	 */
	private boolean tieneMoraComprobante;

	// Getters and setters
	public boolean isValidaBiessEmergente() {
		return validaBiessEmergente;
	}

	public void setValidaBiessEmergente(boolean validaBiessEmergente) {
		this.validaBiessEmergente = validaBiessEmergente;
	}

	public BigDecimal getAportacionesAcumuladas() {
		return aportacionesAcumuladas;
	}

	public void setAportacionesAcumuladas(BigDecimal aportacionesAcumuladas) {
		this.aportacionesAcumuladas = aportacionesAcumuladas;
	}

	public BigDecimal getAportacionesConsecutivas() {
		return aportacionesConsecutivas;
	}

	public void setAportacionesConsecutivas(BigDecimal aportacionesConsecutivas) {
		this.aportacionesConsecutivas = aportacionesConsecutivas;
	}

	public boolean isTieneMoraPlanillas() {
		return tieneMoraPlanillas;
	}

	public void setTieneMoraPlanillas(boolean tieneMoraPlanillas) {
		this.tieneMoraPlanillas = tieneMoraPlanillas;
	}

	public boolean isTieneMoraComprobante() {
		return tieneMoraComprobante;
	}

	public void setTieneMoraComprobante(boolean tieneMoraComprobante) {
		this.tieneMoraComprobante = tieneMoraComprobante;
	}

}
