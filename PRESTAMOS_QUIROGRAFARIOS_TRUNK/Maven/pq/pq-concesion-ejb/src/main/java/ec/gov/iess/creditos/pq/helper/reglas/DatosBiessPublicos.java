public class DatosBiessPublicos {
	
	private boolean validaPublicos;
	
	private boolean tieneMoraComprobantePublicos;

	public boolean isValidaPublicos() {
		return validaPublicos;
	}

	public void setValidaPublicos(boolean validaPublicos) {
		this.validaPublicos = validaPublicos;
	}

	public boolean isTieneMoraComprobantePublicos() {
		return tieneMoraComprobantePublicos;
	}

	public void setTieneMoraComprobantePublicos(boolean tieneMoraComprobantePublicos) {
		this.tieneMoraComprobantePublicos = tieneMoraComprobantePublicos;
	}

}
