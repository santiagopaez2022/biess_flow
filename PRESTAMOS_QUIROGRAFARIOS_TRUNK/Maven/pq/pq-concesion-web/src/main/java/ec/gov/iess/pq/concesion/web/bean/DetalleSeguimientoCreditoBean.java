package ec.gov.iess.pq.concesion.web.bean;

import java.util.Date;
/**
* Clase que se usa para presentar el Detalle Seguimiento Credito
*
*@author rtituana
*/

public class DetalleSeguimientoCreditoBean {
	private Date fechaCambioEstado;
	private String estado;
	private String observacion;
	
	public DetalleSeguimientoCreditoBean() {
	
	}

	public DetalleSeguimientoCreditoBean(Date fechaCambioEstado, String estado,
			String observacion) {
		super();
		this.fechaCambioEstado = fechaCambioEstado;
		this.estado = estado;
		this.observacion = observacion;
	}

	public Date getFechaCambioEstado() {
		return fechaCambioEstado;
	}

	public void setFechaCambioEstado(Date fechaCambioEstado) {
		this.fechaCambioEstado = fechaCambioEstado;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public String getObservacion() {
		return observacion;
	}

	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	
	
}
