package ec.gov.iess.pq.concesion.web.exception;

/**
 * Excepcion que se lanza cuando existe un impedimento de generar un comprobante
 * 
 * @author Roberto Guizado
 * @version 1.0
 * 
 */
public class GeneracionComprobanteExcepcion extends Exception {

	private static final long serialVersionUID = 8881310385776903676L;

	public GeneracionComprobanteExcepcion() {
	}

	public GeneracionComprobanteExcepcion(String arg0) {
		super(arg0);
	}

	public GeneracionComprobanteExcepcion(Throwable arg0) {
		super(arg0);
	}

	public GeneracionComprobanteExcepcion(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
